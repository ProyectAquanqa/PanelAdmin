/**
 * Funci√≥n para limpiar cache del navegador relacionado con usuarios
 */
export const clearUserCache = () => {
  console.log('üßπ LIMPIANDO CACHE DE USUARIOS');
  
  // Limpiar LocalStorage relacionado
  const keysToRemove = [];
  for (let i = 0; i < localStorage.length; i++) {
    const key = localStorage.key(i);
    if (key && (key.includes('user') || key.includes('users'))) {
      keysToRemove.push(key);
    }
  }
  
  keysToRemove.forEach(key => {
    localStorage.removeItem(key);
    console.log(`üóëÔ∏è Eliminado: ${key}`);
  });
  
  // Limpiar SessionStorage relacionado
  const sessionKeysToRemove = [];
  for (let i = 0; i < sessionStorage.length; i++) {
    const key = sessionStorage.key(i);
    if (key && (key.includes('user') || key.includes('users'))) {
      sessionKeysToRemove.push(key);
    }
  }
  
  sessionKeysToRemove.forEach(key => {
    sessionStorage.removeItem(key);
    console.log(`üóëÔ∏è Session eliminado: ${key}`);
  });
  
  // Limpiar cache de React Query si est√° disponible
  if (window.queryClient) {
    window.queryClient.invalidateQueries(['users']);
    console.log('üîÑ Cache de React Query invalidado');
  }
  
  console.log('‚úÖ Cache limpiado completamente');
  console.log('üí° Recarga la p√°gina para aplicar los cambios');
};

/**
 * Funci√≥n para mostrar estad√≠sticas de usuarios
 */
export const showUserStats = async () => {
  console.log('üìä ESTAD√çSTICAS DE USUARIOS');
  console.log('=' .repeat(50));
  
  const workingEndpoint = await testUserConnection();
  if (!workingEndpoint) {
    console.error('‚ùå No se puede obtener estad√≠sticas sin conexi√≥n');
    return;
  }
  
  const token = localStorage.getItem('authToken');
  
  try {
    const response = await fetch(workingEndpoint, {
      headers: {
        'Authorization': `Bearer ${token}`,
        'Content-Type': 'application/json'
      }
    });
    
    if (response.ok) {
      const data = await response.json();
      const users = Array.isArray(data) ? data : data.results || data.data || [];
      
      console.log(`üë• Total de usuarios: ${users.length}`);
      console.log(`‚úÖ Usuarios activos: ${users.filter(u => u.is_active).length}`);
      console.log(`‚ùå Usuarios inactivos: ${users.filter(u => !u.is_active).length}`);
      
      // Estad√≠sticas por rol
      const roleStats = users.reduce((acc, user) => {
        acc[user.role] = (acc[user.role] || 0) + 1;
        return acc;
      }, {});
      
      console.log('\nüìä Por rol:');
      Object.entries(roleStats).forEach(([role, count]) => {
        const percentage = ((count / users.length) * 100).toFixed(1);
        console.log(`  ${role}: ${count} (${percentage}%)`);
      });
      
      // Estad√≠sticas de contacto
      const withPhone = users.filter(u => u.phone && u.phone.trim()).length;
      console.log(`\nüì± Con tel√©fono: ${withPhone}/${users.length}`);
      console.log(`üìß Todos tienen email: ${users.length}/${users.length}`);
      
    } else {
      console.error('‚ùå Error al obtener datos para estad√≠sticas');
    }
  } catch (error) {
    console.error('‚ùå Error:', error);
  }
};

